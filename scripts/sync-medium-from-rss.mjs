import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import admin from "firebase-admin";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const FEED_URL = "https://medium.com/feed/@thabheloduve";
const FALLBACK_COVERS = [
  "/media/blog-mdx-writing.jpg",
  "/media/blog-learning-code.jpg",
  "/media/blog-terminal-setup.jpg",
  "/media/blog-performance-ring.jpg",
];

function loadEnv() {
  const envPath = resolve(root, ".env");
  if (!existsSync(envPath)) return;
  for (const line of readFileSync(envPath, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const separator = trimmed.indexOf("=");
    if (separator === -1) continue;
    const key = trimmed.slice(0, separator).trim();
    const value = trimmed.slice(separator + 1).trim();
    if (!process.env[key]) process.env[key] = value;
  }
}

function stripHtml(html) {
  return html.replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim();
}

function extractCover(html, index) {
  if (!html) return FALLBACK_COVERS[index % FALLBACK_COVERS.length];
  const images = [...html.matchAll(/<img[^>]+src="([^"]+)"/g)].map((match) => match[1]);
  const cover = images.find((src) => src.includes("cdn-images") || src.includes("miro.medium.com"));
  return cover ?? FALLBACK_COVERS[index % FALLBACK_COVERS.length];
}

function estimateReadTime(text) {
  const words = text.split(/\s+/).filter(Boolean).length;
  return `${Math.max(1, Math.round(words / 200))} min read`;
}

function parseRss(xml) {
  const items = [...xml.matchAll(/<item>([\s\S]*?)<\/item>/g)].map((match) => match[1]);

  return items.map((item, index) => {
    const title = item.match(/<title><!\[CDATA\[([\s\S]*?)\]\]><\/title>/)?.[1]
      ?? item.match(/<title>([\s\S]*?)<\/title>/)?.[1]
      ?? "Untitled";
    const link = (item.match(/<link>([\s\S]*?)<\/link>/)?.[1] ?? "").split("?")[0].trim();
    const pubDate = item.match(/<pubDate>([\s\S]*?)<\/pubDate>/)?.[1] ?? "";
    const content = item.match(/<content:encoded><!\[CDATA\[([\s\S]*?)\]\]><\/content:encoded>/)?.[1] ?? "";
    const slug = link.replace(/\/$/, "").split("/").pop() ?? `post-${index}`;
    const plain = stripHtml(content);
    const excerpt = plain.length > 180 ? `${plain.slice(0, 180).trim()}…` : plain;

    return {
      slug,
      title: title.trim(),
      date: pubDate ? new Date(pubDate).toISOString().split("T")[0] : "2024-01-01",
      excerpt: excerpt || `Read ${title.trim()} on Medium.`,
      readTime: estimateReadTime(plain),
      url: link,
      coverImage: extractCover(content, index),
      tags: ["Medium", "Tech"],
      channel: "medium",
    };
  });
}

function serializePosts(posts) {
  return `/** Auto-generated by scripts/sync-medium-from-rss.mjs — do not edit manually */\n\nexport const mediumPosts = ${JSON.stringify(posts, null, 2)} as const;\n\nexport type MediumPostConfig = (typeof mediumPosts)[number];\n`;
}

async function syncFirestore(posts) {
  const keyPath = resolve(root, ".firebase-seed-key.json");
  if (!existsSync(keyPath)) {
    console.log("⚠ Skipping Firestore sync — missing .firebase-seed-key.json");
    return;
  }

  if (!admin.apps.length) {
    admin.initializeApp({
      credential: admin.credential.cert(JSON.parse(readFileSync(keyPath, "utf8"))),
      storageBucket: "thabhelo-portfolio.firebasestorage.app",
    });
  }

  const db = admin.firestore();
  const existing = await db.collection("mediumPosts").get();
  const batch = db.batch();
  existing.docs.forEach((doc) => batch.delete(doc.ref));
  await batch.commit();

  for (const post of posts) {
    await db.collection("mediumPosts").doc(post.slug).set(post);
    console.log(`✓ Firestore: ${post.slug}`);
  }
}

async function main() {
  loadEnv();
  console.log(`Fetching ${FEED_URL}...\n`);

  const response = await fetch(FEED_URL);
  if (!response.ok) throw new Error(`Feed request failed: ${response.status}`);

  const xml = await response.text();
  const posts = parseRss(xml);
  if (!posts.length) throw new Error("No Medium posts found in RSS feed");

  const outputPath = resolve(root, "src/config/medium-posts.ts");
  writeFileSync(outputPath, serializePosts(posts));
  console.log(`✓ Wrote ${posts.length} posts to src/config/medium-posts.ts\n`);

  posts.forEach((post) => console.log(`  • ${post.title}\n    ${post.url}`));

  console.log("");
  await syncFirestore(posts);
  console.log("\nDone.");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
